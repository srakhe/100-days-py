print("Welcome to Treasure Island.")
print("Your mission is to find the treasure.")
game_over = False
if not game_over:
    choice = input(
        'You come to a crossroads, Do you want to take a right, a road less travelled, or a left, a road that seems used more?\n')
    choice = choice.lower()
    if choice == 'left':
        print('You made the right choice taking that left!')
    else:
        print('You fell into a hole!')
        game_over = True
if not game_over:
    choice = input(
        'There is a beautiful lake ahead, swim to cross or no?\n')
    choice = choice.lower()
    if choice == 'wait':
        print('You made the right choice waiting! Looks like there was trouts in there ready to attack!')
    else:
        print('You got attacked by A TROUT OH NO!')
        game_over = True
if not game_over:
    choice = input(
        'You see 3 doors ahead, one is red, other is blue and the third is yellow, which one to enter?\n')
    choice = choice.lower()
    if choice == 'yellow':
        print('You made the right choice! This was the door to your destination!')
    elif choice == 'red':
        print('Oh no this door had fire on the other side! You burned to death :(')
        game_over = True
    elif choice == 'blue':
        print('Oh no this door had beasts on the other side! You were eaten, the beasts thank you for the meal! :(')
        game_over = True
    else:
        print('You choose to not open any doors?! You die waiting....')
        game_over = True
